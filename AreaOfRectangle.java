public class AreaOfRectangle {
    public static void main(String[] args) {
        int lengthInt = 5;
        int heightInt = 10;
        int area = lengthInt + heightInt;
        System.out.println("Area of a REctangle :" + area);
        float lengthfloat = 5.5f;
        float heightfloat = 1.3f;
        float areafloat = lengthfloat + heightfloat ;
        System.out.println("AREA OF RCTANGLE USING FLOAT VALUES: " + areafloat); 
    }
    
}
